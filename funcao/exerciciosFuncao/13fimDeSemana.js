function finds(dia) {
    switch (dia) {
        case 1:
            return 'Fim de semana'
        case 2:
            return 'Dia de semana'
        case 3:
            return 'Dia de semana'
        case 4:
            return 'Dia de semana'
        case 5:
            return 'Dia de semana'
        case 6:
            return 'Dia de semana'
        case 7:
            return 'Fim de semana'
        default:

            break;
    }
}

